interface CryptographicAsset {
  id: string;
  algorithm: string;
  keyStrength?: number;
  libraryName: string;
  libraryVersion: string;
  discoverySource: string;
  fips140_3Compliant: boolean;
  risk: "Critical" | "High" | "Medium" | "Low";
  type: "Library Capability" | "Hard-coded Insecure Crypto";
  libraryCapabilities?: string[]; // Algorithms the library supports
  detectedUsage?: string[]; // Algorithms actually detected in code
  // CBOM-specific fields (from CycloneDX CBOM format)
  primitive?: string; // e.g., "hash", "signature", "pke", "drbg", "key-agree", "kdf"
  cryptoFunctions?: string[]; // e.g., ["digest"], ["sign", "verify"], ["encrypt", "decrypt"]
  occurrences?: Array<{
    location: string;
    line: number;
    context?: string;
  }>;
  // SBOMs that use this cryptographic asset
  sboms?: Array<{
    id: string;
    name: string;
  }>;
}

