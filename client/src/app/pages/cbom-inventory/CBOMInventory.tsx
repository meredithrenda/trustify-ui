// - Minimum requirement: Discoverability of library/package support (name + version)
// - Ideal: Visibility into missing components (what library supports but isn't used)
// - Current focus: Visibility and inventory (no remediations yet)
// - Future: Migration tracking, policy-based admission control, Insights integration
//
// Real CBOM Integration Notes:
// - CBOM files (CycloneDX format) contain cryptographic-asset components with:
//   - name: algorithm name (e.g., "SHA-1", "RSA", "ECDSA-P256")
//   - cryptoProperties.assetType: "algorithm" or "related-crypto-material"
//   - cryptoProperties.algorithmProperties.primitive: "hash", "signature", "pke", "drbg", "key-agree", "kdf"
//   - cryptoProperties.algorithmProperties.parameterSetIdentifier: key size (e.g., "256", "2048", "160")
//   - cryptoProperties.algorithmProperties.cryptoFunctions: ["digest"], ["sign", "verify"], ["encrypt", "decrypt"]
//   - evidence.occurrences: array of file locations and line numbers where detected
//   - evidence.identity[].methods[].value: discovery source (e.g., "scanoss:ruleid,...")
//
// To integrate real CBOM data:
// 1. Parse CycloneDX CBOM JSON files (generated by Scanoss)
// 2. Map CBOM components to CryptographicAsset interface
// 3. Determine risk level based on algorithm (SHA-1, MD5, weak RSA = Critical/High)
// 4. Determine type: "Hard-coded Insecure Crypto" if detected in code (evidence.occurrences present),
//    "Library Capability" if from dependency analysis (library supports but may not be used)
// 5. Extract library info from associated SBOM dependencies
// 6. Check FIPS 140-3 compliance based on algorithm and key strength
// 7. Identify missing components: Compare libraryCapabilities vs detectedUsage to show
//    what the library supports but isn't being used (addresses "potential for inclusion" requirement)
//
